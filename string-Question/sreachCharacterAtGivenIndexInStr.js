//! 10.Write a function that returns the character at a given index in a string. (Easy) 
// Explanation: Use bracket notation str[index] or 
// charAt(index) to get the character.

let str = "shreya";

function sreachIndexCharacter(str){
    // return str[0];
    return str.charAt(5);
}
console.log(sreachIndexCharacter(str));
